
public class Ejercicio04 {
	
	public static void main (String[] args) {
		
		System.out.println("Números del 320 al 160 restando 20 hacia atrás con for");
		System.out.println("-------------------------------------------------------");
		
		for (int i = 320; i >= 160; i-=20) {
			System.out.println("- " + i);
		}
	}
}

