
public class Ejercicio05 {
	
	public static void main (String[] args) {
		
		System.out.println("Números del 320 al 160 restando 20 hacia atrás con while");
		System.out.println("---------------------------------------------------------");
		
		int i = 320;
		
		while (i >=160) {
			System.out.println("- " + i);
			i-=20;
		}
	}
}

